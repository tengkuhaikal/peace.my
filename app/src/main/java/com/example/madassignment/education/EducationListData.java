package com.example.madassignment.education;

public class EducationListData {
    int name;
    int image, icon;
    int description, symptoms, treatment;

    public EducationListData(int name, int image, int description, int symptoms, int treatment, int icon) {
        this.name = name;
        this.image = image;
        this.description = description;
        this.symptoms = symptoms;
        this.treatment = treatment;
        this.icon = icon;
    }
}
